namespace FluentJalium.Icon;

/// <summary>
/// Identifies the icon font family used by <see cref="FluentIcon"/>.
/// </summary>
public enum FluentIconSet
{
    /// <summary>
    /// Uses Fluent UI System Icons Regular.
    /// </summary>
    Regular,

    /// <summary>
    /// Uses Fluent UI System Icons Filled.
    /// </summary>
    Filled,

    /// <summary>
    /// Uses Windows Segoe Fluent Icons compatibility glyphs.
    /// </summary>
    Segoe
}
